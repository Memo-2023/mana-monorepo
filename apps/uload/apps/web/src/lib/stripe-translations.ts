// Helper function to get translations for current locale
export function getStripeTranslation(locale: string) {
	// Default to English if locale not found
	return stripeTranslations[locale as keyof typeof stripeTranslations] || stripeTranslations.en;
}

// Get locale-specific Stripe configuration
export function getStripeConfig(locale: string) {
	const isEuroZone = ['de', 'it', 'fr', 'es'].includes(locale);

	return {
		currency: isEuroZone ? 'eur' : 'usd',
		locale: locale === 'en' ? 'en' : locale,
		// Map our locale codes to Stripe's expected format
		stripeLocale:
			{
				en: 'en',
				de: 'de',
				it: 'it',
				fr: 'fr',
				es: 'es'
			}[locale] || 'auto'
	};
}
